import json
from mistralai.client import Mistral

def evaluate_trajectory(
    client: Mistral,
    goal: str,
    trajectory: list[dict],
    missing_skills: list[str]
) -> dict:
    """
    The LLM evaluates the quality of a trajectory generated by the graph engine.
    
    This is the LLM integration as evaluator: the algorithm generates the trajectory,
    but the LLM "reviews" it from a semantic/professional point of view.
    
    The LLM returns:
      - score: number from 0 to 10
      - strengths: what is good about the trajectory
      - weaknesses: what may be missing or could be improved
      - uncovered_skills: skills from the goal that no course covers
      - general_recommendation: professional advice text
    
    Returns: dict with the above fields, or empty dict on error.
    """
    # Prepare the trajectory summary for the prompt
    course_summary = "\n".join([
        f"  {i+1}. {c['nombre']} ({c['duracion_semanas']} weeks, {c['coste']}€) → provides: {', '.join(c['habilidades_aportadas'])}"
        for i, c in enumerate(trajectory)
    ])
    
    prompt = f"""
    Eres un experto en desarrollo profesional y planificación de carreras tecnológicas.

    Un sistema de planificación automática ha generado la siguiente trayectoria de aprendizaje:

    META PROFESIONAL: "{goal}"

    HABILIDADES QUE NECESITA ADQUIRIR: {', '.join(missing_skills)}

    TRAYECTORIA PROPUESTA (en orden de aprendizaje):
    {course_summary}

    Evalúa esta trayectoria y responde ÚNICAMENTE con un JSON con exactamente estas claves:
    {{
    "puntuacion": <número del 1 al 10>,
    "puntos_fuertes": ["punto 1", "punto 2"],
    "puntos_debiles": ["debilidad 1", "debilidad 2"],
    "habilidades_no_cubiertas": ["habilidad A", "habilidad B"],
    "recomendacion_general": "texto de consejo profesional (2-3 frases)"
    }}

    Criterios de evaluación:
    - ¿Cubre las habilidades clave para la meta?
    - ¿El orden de los cursos es lógico (no hay saltos extraños)?
    - ¿Es una carga de trabajo razonable?
    - ¿Faltan habilidades importantes que no están en la lista?
    - ¿Hay cursos innecesarios o redundantes?

    Responde SOLO con JSON, sin texto adicional.
    """
    
    try:
        completion = client.chat.complete(
            model="mistral-small-latest",
            messages=[{"role": "user", "content": prompt}],
            response_format={"type": "json_object"},
            temperature=0.2   # Slightly more temperature for more nuanced opinions
        )
        response = completion.choices[0].message.content
        return json.loads(response)
    except Exception as e:
        print(f"   ❌ Error in LLM evaluation: {e}")
        return {}